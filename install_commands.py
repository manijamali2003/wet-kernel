import os

def shut():
    file = open("commands/shut.py", "r")
    strv = file.read()
    file.close()
    vmwet_file = open("commands.py", "a")
    vmwet_file.write(strv + "\n")
    vmwet_file.close()
    file = open("jobs/job_commands/shut", "w")
    file.close()

if not os.path.isfile ("jobs/job_commands/shut"):
    shut()

